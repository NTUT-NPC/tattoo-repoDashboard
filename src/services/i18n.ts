export type MessageKey = keyof typeof MESSAGES.en;

function template(value: string, params?: Record<string, string | number>): string {
  if (!params) return value;
  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function t(key: MessageKey, params?: Record<string, string | number>): string {
  const dictionary = MESSAGES[resolvedLocale.value];
  const value = (dictionary as Record<string, string>)[key] ?? (MESSAGES.en as Record<string, string>)[key] ?? key;
  return template(value, params);
}

export function setLanguageMode(mode: LanguageMode) {
  languageMode.value = mode;
  window.localStorage.setItem(LANGUAGE_MODE_STORAGE_KEY, mode);
}

export function useI18n() {
  return {
    languageMode,
    resolvedLocale,
    intlLocale,
    t,
    setLanguageMode,
  };
}

