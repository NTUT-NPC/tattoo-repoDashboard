import { computed, ref } from 'vue';

export type LanguageMode = 'auto' | 'zh' | 'en' | 'wzh';
export type ResolvedLocale = 'zh' | 'en' | 'wzh';

const LANGUAGE_MODE_STORAGE_KEY = 'tattoo-dashboard-language-mode';

function readLanguageModeFromStorage(): LanguageMode {
  const raw = window.localStorage.getItem(LANGUAGE_MODE_STORAGE_KEY);
  if (raw === 'zh' || raw === 'en' || raw === 'wzh' || raw === 'auto') return raw;
  return 'auto';
}

function getBrowserResolvedLocale(): ResolvedLocale {
  const raw = (navigator.languages?.[0] ?? navigator.language ?? '').toLowerCase();
  // 若瀏覽器為中文，預設採用文言模式
  if (raw.startsWith('zh')) return 'wzh';
  return 'en';
}

const languageMode = ref<LanguageMode>(typeof window === 'undefined' ? 'auto' : readLanguageModeFromStorage());

const resolvedLocale = computed<ResolvedLocale>(() => {
  if (languageMode.value === 'auto') return getBrowserResolvedLocale();
  return languageMode.value;
});

const intlLocale = computed(() => (resolvedLocale.value === 'zh' || resolvedLocale.value === 'wzh' ? 'zh-TW' : 'en-US'));

